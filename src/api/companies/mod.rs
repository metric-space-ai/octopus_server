use crate::{
    context::Context,
    entity::{Company, ROLE_COMPANY_ADMIN_USER},
    error::{AppError, ResponseError},
    session::{require_authenticated, ExtractedSession},
};
use axum::{
    extract::{Path, State},
    http::StatusCode,
    response::IntoResponse,
    Json,
};
use serde::Deserialize;
use std::sync::Arc;
use utoipa::ToSchema;
use uuid::Uuid;
use validator::Validate;

#[derive(Debug, Deserialize, ToSchema, Validate)]
pub struct CompanyPut {
    #[validate(length(max = 256, min = 1))]
    pub address: Option<String>,
    pub allowed_domains: Option<Vec<String>>,
    pub custom_style: Option<String>,
    #[validate(length(max = 256, min = 1))]
    pub name: String,
}

#[axum_macros::debug_handler]
#[utoipa::path(
    get,
    path = "/api/v1/companies/:id",
    responses(
        (status = 200, description = "Company read.", body = Company),
        (status = 401, description = "Unauthorized.", body = ResponseError),
        (status = 403, description = "Forbidden.", body = ResponseError),
        (status = 404, description = "Company not found.", body = ResponseError),
    ),
    params(
        ("id" = String, Path, description = "Company id")
    ),
    security(
        ("api_key" = [])
    )
)]
pub async fn read(
    State(context): State<Arc<Context>>,
    extracted_session: ExtractedSession,
    Path(id): Path<Uuid>,
) -> Result<impl IntoResponse, AppError> {
    let session = require_authenticated(extracted_session).await?;

    let session_user = context
        .octopus_database
        .try_get_user_by_id(session.user_id)
        .await?
        .ok_or(AppError::Forbidden)?;

    let company = context
        .octopus_database
        .try_get_company_by_id(id)
        .await?
        .ok_or(AppError::NotFound)?;

    if session_user.company_id != id {
        return Err(AppError::Forbidden);
    }

    Ok((StatusCode::OK, Json(company)).into_response())
}

#[axum_macros::debug_handler]
#[utoipa::path(
    put,
    path = "/api/v1/companies/:id",
    request_body = CompanyPut,
    responses(
        (status = 200, description = "Company updated.", body = Company),
        (status = 401, description = "Unauthorized.", body = ResponseError),
        (status = 403, description = "Forbidden.", body = ResponseError),
        (status = 404, description = "Company not found.", body = ResponseError),
    ),
    params(
        ("id" = String, Path, description = "Company id")
    ),
    security(
        ("api_key" = [])
    )
)]
pub async fn update(
    State(context): State<Arc<Context>>,
    extracted_session: ExtractedSession,
    Path(id): Path<Uuid>,
    Json(input): Json<CompanyPut>,
) -> Result<impl IntoResponse, AppError> {
    let session = require_authenticated(extracted_session).await?;
    input.validate()?;

    let session_user = context
        .octopus_database
        .try_get_user_by_id(session.user_id)
        .await?
        .ok_or(AppError::Forbidden)?;

    let company = context
        .octopus_database
        .try_get_company_by_id(id)
        .await?
        .ok_or(AppError::NotFound)?;

    if session_user.company_id != id
        || !session_user
            .roles
            .contains(&ROLE_COMPANY_ADMIN_USER.to_string())
    {
        return Err(AppError::Forbidden);
    }

    let allowed_domains = input
        .allowed_domains
        .filter(|allowed_domains| !allowed_domains.is_empty());

    let mut transaction = context.octopus_database.transaction_begin().await?;

    let company = context
        .octopus_database
        .update_company(
            &mut transaction,
            company.id,
            input.address,
            allowed_domains,
            input.custom_style,
            &input.name,
        )
        .await?;

    context
        .octopus_database
        .transaction_commit(transaction)
        .await?;

    Ok((StatusCode::OK, Json(company)).into_response())
}

