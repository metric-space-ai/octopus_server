use crate::{
    context::Context,
    error::AppError,
    session::{ensure_secured, require_authenticated_session, ExtractedSession},
};
use axum::{
    extract::{Path, State},
    http::StatusCode,
    response::IntoResponse,
    Json,
};
use serde::Deserialize;
use std::sync::Arc;
use utoipa::ToSchema;
use uuid::Uuid;
use validator::Validate;

#[derive(Debug, Deserialize, ToSchema, Validate)]
pub struct ExamplePromptPost {
    pub is_visible: bool,
    pub priority: i32,
    pub prompt: String,
}

#[derive(Debug, Deserialize, ToSchema, Validate)]
pub struct ExamplePromptPut {
    pub is_visible: bool,
    pub priority: i32,
    pub prompt: String,
}

#[axum_macros::debug_handler]
#[utoipa::path(
    post,
    path = "/api/v1/example-prompts",
    request_body = ExamplePromptPost,
    responses(
        (status = 201, description = "Create example prompt", body = ExamplePrompt),
        (status = 401, description = "Unauthorized", body = ResponseError)
    ),
    security(
        ("api_key" = [])
    )
)]
pub async fn create(
    State(context): State<Arc<Context>>,
    extracted_session: ExtractedSession,
    Json(input): Json<ExamplePromptPost>,
) -> Result<impl IntoResponse, AppError> {
    ensure_secured(context.clone(), extracted_session, "ROLE_COMPANY_ADMIN").await?;
    input.validate()?;

    let example_prompt = context
        .octopus_database
        .insert_example_prompt(input.is_visible, input.priority, &input.prompt)
        .await?;

    Ok((StatusCode::CREATED, Json(example_prompt)).into_response())
}

#[axum_macros::debug_handler]
#[utoipa::path(
    delete,
    path = "/api/v1/example-prompts/:id",
    responses(
        (status = 204, description = "Example prompt delete"),
        (status = 401, description = "Unauthorized", body = ResponseError),
        (status = 404, description = "Example prompt not found", body = ResponseError)
    ),
    params(
        ("id" = String, Path, description = "Example prompt id")
    ),
    security(
        ("api_key" = [])
    )
)]
pub async fn delete(
    State(context): State<Arc<Context>>,
    extracted_session: ExtractedSession,
    Path(id): Path<Uuid>,
) -> Result<impl IntoResponse, AppError> {
    ensure_secured(context.clone(), extracted_session, "ROLE_COMPANY_ADMIN").await?;

    let example_prompt = context
        .octopus_database
        .try_delete_example_prompt_by_id(id)
        .await?;

    match example_prompt {
        None => Err(AppError::NotFound),
        Some(_example_prompt) => Ok((StatusCode::NO_CONTENT, ()).into_response()),
    }
}

#[axum_macros::debug_handler]
#[utoipa::path(
    get,
    path = "/api/v1/example-prompts",
    responses(
        (status = 200, description = "List all example prompts", body = [ExamplePrompt]),
        (status = 401, description = "Unauthorized", body = ResponseError)
    ),
    security(
        ("api_key" = [])
    )
)]
pub async fn list(
    State(context): State<Arc<Context>>,
    extracted_session: ExtractedSession,
) -> Result<impl IntoResponse, AppError> {
    require_authenticated_session(extracted_session).await?;

    let example_prompts = context.octopus_database.get_example_prompts().await?;

    Ok((StatusCode::OK, Json(example_prompts)).into_response())
}

